void MmlParser::onTagEnd()
{
	if(!isProcessingTag) return;
	std::string commandText = commandStream.str();
	// convert string to upper case
	std::for_each(std::begin(commandText), std::end(commandText), [](char & c){
		c = ::tolower(c);
	});
	commandText.erase(std::remove_if(std::begin(commandText), std::end(commandText), ::isspace), std::end(commandText));
	commandStream.str("");
	isProcessingTag = false;
	processTag(commandText);
}

static void mmlNoop(MmlParser& parser) {
	(void)parser;
}
