void MmlParser::parse(char c)
{
	switch (c) {
		case '<': isProcessingTag = true; break;
		case '>': onTagEnd(); break;
		case '[': isProcessingTag = true; break;
		case ']': onTagEnd(); break;
		case '&': isProcessingEscape = true; break;
		case ';': {
			if(isProcessingEscape) onEscapeEnd();
			else *RTP << char(c);
			break;
		}
		default: {
			if(isProcessingTag || isProcessingEscape) commandStream << c;
			else *RTP << char(c);
			break;
		}
	}
}

void MmlParser::parse(const char* cstr)
{
	for(;*cstr;++cstr) {
		parse(*cstr);
	}
}

void MmlParser::parse(const std::string& str)
{
	for(const auto c : str) {
		parse(c);
	}
}
